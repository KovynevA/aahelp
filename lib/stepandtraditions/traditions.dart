import 'package:aahelp/helper/stylemenu.dart';
import 'package:flutter/material.dart';

class Traditions12 extends StatelessWidget {
  const Traditions12({super.key});

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      body: ListView.builder(
        padding: const EdgeInsets.all(16),
        itemCount: traditions.length,
        itemBuilder: (context, index) {
          return TraditionCard(
            tradition: traditions[index],
            index: index,
          );
        },
      ),
    );
  }
}

class TraditionCard extends StatefulWidget {
  final Map<String, String> tradition;
  final int index;

  const TraditionCard({
    super.key,
    required this.tradition,
    required this.index,
  });

  @override
  State<TraditionCard> createState() => _TraditionCardState();
}

class _TraditionCardState extends State<TraditionCard> {
  bool _isExpanded = false;

  @override
  Widget build(BuildContext context) {
    return Card(
      margin: const EdgeInsets.only(bottom: 16),
      elevation: 4,
      shape: RoundedRectangleBorder(
        borderRadius: BorderRadius.circular(12),
      ),
      child: Padding(
        padding: const EdgeInsets.all(16),
        child: Column(
          crossAxisAlignment: CrossAxisAlignment.center,
          children: [
            Row(
              children: [
                Container(
                  width: 36,
                  height: 36,
                  decoration: BoxDecoration(
                    color:
                        Theme.of(context).primaryColor.withValues(alpha: 0.2),
                    shape: BoxShape.circle,
                  ),
                  child: Center(
                    child: BeautifulText(
                      text: '${widget.index + 1}',
                      fontWeight: FontWeight.bold,
                      fontSize: 16,
                      color: Colors.indigo,
                    ),
                  ),
                ),
                Expanded(
                  child: BeautifulText(
                    text: 'традиция',
                    fontSize: 18,
                    color: Colors.indigo,
                  ),
                ),
              ],
            ),
            const SizedBox(height: 12),
            BeautifulText(
              text: widget.tradition['short']!,
              fontSize: 16,
              textAlign: TextAlign.justify,
              color: Colors.black,
            ),
            if (widget.tradition['long'] != null) ...[
              const SizedBox(height: 4),
              AnimatedCrossFade(
                duration: const Duration(milliseconds: 300),
                crossFadeState: _isExpanded
                    ? CrossFadeState.showSecond
                    : CrossFadeState.showFirst,
                firstChild: TextButton(
                  onPressed: () => setState(() => _isExpanded = true),
                  child: const Row(
                    mainAxisSize: MainAxisSize.min,
                    children: [
                      SizedBox(
                        width: 36,
                      ),
                      BeautifulText(
                        text: 'Подробнее',
                        fontSize: 16,
                        color: Colors.blueGrey,
                      ),
                      SizedBox(width: 4),
                      Icon(Icons.expand_more, size: 20),
                    ],
                  ),
                ),
                secondChild: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    BeautifulText(
                      text: widget.tradition['long']!,
                      fontSize: 15,
                      color: Colors.brown,
                      textAlign: TextAlign.justify,
                    ),
                    const SizedBox(height: 8),
                    TextButton(
                      onPressed: () => setState(() => _isExpanded = false),
                      child: const Row(
                        mainAxisSize: MainAxisSize.min,
                        children: [
                          BeautifulText(
                            text: 'Свернуть',
                            fontSize: 16,
                            color: Colors.blueGrey,
                          ),
                          SizedBox(width: 4),
                          Icon(Icons.expand_less, size: 20),
                        ],
                      ),
                    ),
                  ],
                ),
              ),
            ],
          ],
        ),
      ),
    );
  }
}

